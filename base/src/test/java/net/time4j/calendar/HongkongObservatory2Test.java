    private static final CalendarSystem<ChineseCalendar> CALSYS = ChineseCalendar.axis().getCalendarSystem();

    private ChineseCalendar chinese;
    private long epoch;

    public HongkongObservatory2Test(
        int year,
        int month,
        String iso,
        boolean leap
    ) throws ParseException {
        super();

        EastAsianMonth eam = EastAsianMonth.valueOf(month);
        if (leap) {
            eam = eam.withLeap();
        }
        this.chinese = ChineseCalendar.of(EastAsianYear.forGregorian(year), eam, 1);
        this.epoch = Iso8601Format.EXTENDED_CALENDAR_DATE.parse(iso).getDaysSinceEpochUTC();

    }

    @Test
    public void fromChinese() {
        assertThat(
            CALSYS.transform(this.chinese),
            is(this.epoch));
    }

    @Test
    public void toChinese() {
        assertThat(
            CALSYS.transform(this.epoch),
            is(this.chinese));
    }

